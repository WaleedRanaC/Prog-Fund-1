#mile
def convertMiles(mi):
    kilometer=1.6
    lengthConversion=mi*kilometer
    return lengthConversion
#temp
def convertFarenheit(fah):
    tempConversion=(fah-32)*5/9
    return tempConversion
#volume
def convertGallon(gal):
    liter=3.9
    volumeConversion=gal*liter
    return volumeConversion
#mass
def convertPound(lb):
    kilogram=.45
    massConversion=lb*kilogram
    return massConversion
#inch
def convertInch(inch):
    centimeter=2.54
    inchConversion=centimeter*inch
    return inchConversion
